package database;

public class DTO {

	String username;
	String localhost;
	
	public String getUsername() {
		return username;
	}
	public String getLocalhost() {
		return localhost;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public void setLocalhost(String localhost) {
		this.localhost = localhost;
	}	
}
